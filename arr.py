def sum_array(arr):
    # Проверяем ограничения на длину массива
    if not 1 <= len(arr) <= 100:
        raise ValueError("Длина массива должна быть от 1 до 100")
    
    # Проверяем ограничения на значения элементов
    if not all(-1000 <= x <= 1000 for x in arr):
        raise ValueError("Элементы массива должны быть от -1000 до 1000")
    
    # Возвращаем сумму элементов
    return sum(arr)
